create index IF not exists idx_art_posts_user_id on public.art_posts using btree (user_id) TABLESPACE pg_default;

create index IF not exists idx_art_posts_art_id on public.art_posts using btree (art_id) TABLESPACE pg_default;

create index IF not exists idx_art_posts_visibility on public.art_posts using btree (visibility) TABLESPACE pg_default;

create index IF not exists idx_art_posts_created_at on public.art_posts using btree (created_at desc) TABLESPACE pg_default;

create index IF not exists idx_art_posts_public_feed on public.art_posts using btree (created_at desc) TABLESPACE pg_default
where
  (
    (visibility = 'public'::art_post_visibility)
    and (is_archived = false)
  );

create index IF not exists idx_art_posts_user_visibility_created on public.art_posts using btree (user_id, visibility, created_at desc) TABLESPACE pg_default;

