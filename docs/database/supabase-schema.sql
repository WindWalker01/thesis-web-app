-- ART SIMILARITY SCANS

create table public.art_similarity_scans (
  id uuid not null default gen_random_uuid (),
  art_id uuid not null,
  owner_id uuid not null,
  status text not null default 'pending'::text,
  filename text null,
  original_hash text null,
  success boolean not null default false,
  total_matches integer not null default 0,
  best_source text null,
  best_link text null,
  best_url text null,
  best_match_pair text null,
  best_similarity_percentage numeric(5, 2) null,
  best_min_combined_distance numeric(10, 4) null,
  best_average_combined_distance numeric(10, 4) null,
  best_max_combined_distance numeric(10, 4) null,
  matches jsonb not null default '[]'::jsonb,
  hashes jsonb null,
  raw_response jsonb null,
  error_message text null,
  started_at timestamp with time zone null,
  completed_at timestamp with time zone null,
  created_at timestamp with time zone not null default now(),
  updated_at timestamp with time zone not null default now(),
  constraint art_similarity_scans_pkey primary key (id),
  constraint uq_art_similarity_scans_art_id unique (art_id),
  constraint art_similarity_scans_art_id_fkey foreign KEY (art_id) references registered_arts (id) on delete CASCADE,
  constraint art_similarity_scans_owner_id_fkey foreign KEY (owner_id) references users (id) on delete CASCADE,
  constraint art_similarity_scans_best_similarity_percentage_check check (
    (
      (best_similarity_percentage is null)
      or (
        (best_similarity_percentage >= (0)::numeric)
        and (best_similarity_percentage <= (100)::numeric)
      )
    )
  ),
  constraint art_similarity_scans_status_check check (
    (
      status = any (
        array[
          'pending'::text,
          'running'::text,
          'completed'::text,
          'failed'::text,
          'cancelled'::text
        ]
      )
    )
  )
) TABLESPACE pg_default;

create index IF not exists idx_art_similarity_scans_owner_id on public.art_similarity_scans using btree (owner_id) TABLESPACE pg_default;

create index IF not exists idx_art_similarity_scans_status on public.art_similarity_scans using btree (status) TABLESPACE pg_default;

create index IF not exists idx_art_similarity_scans_success on public.art_similarity_scans using btree (success) TABLESPACE pg_default;

create index IF not exists idx_art_similarity_scans_best_similarity on public.art_similarity_scans using btree (best_similarity_percentage desc) TABLESPACE pg_default;

create index IF not exists idx_art_similarity_scans_best_source on public.art_similarity_scans using btree (best_source) TABLESPACE pg_default;

create index IF not exists idx_art_similarity_scans_created_at on public.art_similarity_scans using btree (created_at desc) TABLESPACE pg_default;

create index IF not exists idx_art_similarity_scans_matches_gin on public.art_similarity_scans using gin (matches jsonb_path_ops) TABLESPACE pg_default;

create index IF not exists idx_art_similarity_scans_raw_response_gin on public.art_similarity_scans using gin (raw_response jsonb_path_ops) TABLESPACE pg_default;

create index IF not exists idx_art_similarity_scans_hashes_gin on public.art_similarity_scans using gin (hashes jsonb_path_ops) TABLESPACE pg_default;

